namespace RTEditor
{
    /// <summary>
    /// This enum can be used to identify different types of camera view volume planes.
    /// </summary>
    public enum CameraViewVolumePlane
    {
        /// <summary>
        /// The left plane.
        /// </summary>
        Left = 0,

        /// <summary>
        /// The right plane.
        /// </summary>
        Right,

        /// <summary>
        /// The bottom plane.
        /// </summary>
        Bottom,

        /// <summary>
        /// The top plane.
        /// </summary>
        Top,

        /// <summary>
        /// The near plane.
        /// </summary>
        Near,

        /// <summary>
        /// The far plane.
        /// </summary>
        Far
    }
}